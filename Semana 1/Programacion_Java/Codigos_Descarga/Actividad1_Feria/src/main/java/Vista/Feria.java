    private void btnCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCalcularActionPerformed
        float monto, alquiler, publicidad, transporte, servicios, decoracion, gastos;
        monto = Float.parseFloat(txtCantidad.getText().trim());
        if(monto >0 ){
            alquiler = (monto * 23)/100;
            publicidad = (monto * 7)/100;
            transporte = (monto * 26)/100;
            servicios = (monto * 12)/100;
            decoracion = (monto * 21)/100;
            gastos = (monto * 11)/100;
            
            this.txtDatosInversion.setText("\nLos gatos a invertir son los siguientes");
            this.txtDatosInversion.append("\nAlquiler: S/. " + alquiler);
            this.txtDatosInversion.append("\nPublicidad: S/. " + publicidad);
            this.txtDatosInversion.append("\nTransporte: S/. " + transporte);
            this.txtDatosInversion.append("\nServicios: S/. " + servicios);
            this.txtDatosInversion.append("\nDecoracion: S/. " + decoracion);
            this.txtDatosInversion.append("\nGastos: S/. " + gastos);
        }else{ JOptionPane.showMessageDialog(this, "Ingrese un Valor Mayor a CERO"); 
            this.txtCantidad.setText(""); 
            this.txtCantidad.requestFocus(); }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnCalcularActionPerformed

